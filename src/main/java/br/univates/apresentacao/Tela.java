    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnAdicionarActionPerformed
    {//GEN-HEADEREND:event_btnAdicionarActionPerformed
        String telefone = tfTelefone.getText();
        String nome = tfNome.getText();

        Contato contato = new Contato(telefone, nome);
        this.contatos.add(contato);

        salvarContatosArquivo();

        Object[] dados = {nome, telefone};
        modelTabela.insertRow(modelTabela.getRowCount(), dados);

        tfTelefone.setText("");
        tfNome.setText("");
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnEditarActionPerformed
    {//GEN-HEADEREND:event_btnEditarActionPerformed
        try {
            int index = jTable1.getSelectedRow();
            Contato contato = contatos.get(index);

            TelaEditar telaEditar = new TelaEditar();
            telaEditar.setVisible(true);
            telaEditar.preencherCampos(contato);

        } catch (IndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Selecione um contato antes de editar.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnExcluirActionPerformed
    {//GEN-HEADEREND:event_btnExcluirActionPerformed
        try {
            int linha = jTable1.getSelectedRow();
            String telefone = modelTabela.getValueAt(linha, 1).toString();

            ContatoDao dao = new ContatoDao(contatos);
            dao.deletar(dao.getContato(telefone));

            salvarContatosArquivo();

            modelTabela.removeRow(linha);

        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Selecione um contato antes de excluir.");
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

