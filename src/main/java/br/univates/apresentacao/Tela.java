    public void salvarContatosArquivo()
    {
        ContatoDao dao = new ContatoDao();
        dao.salvar(this.contatos);
    }

    public void inicializarTabela()
    {
        modelTabela = (DefaultTableModel) jTable1.getModel();
        for (int i = 0; i < this.contatos.size(); i++) {
            String telefone = this.contatos.get(i).getTelefone();
            String nome = this.contatos.get(i).getNome();

            Object[] dados = {nome, telefone};
            modelTabela.insertRow(modelTabela.getRowCount(), dados);
        }
    }

    public static void atualizarTabela(int index, String telefone, String nome)
    {
        modelTabela.setValueAt(nome, index, 0);
        modelTabela.setValueAt(telefone, index, 1);
    }

